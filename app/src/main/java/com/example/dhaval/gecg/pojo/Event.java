public class Event {

        private String time;

        private String location;

        private String contact_no;

        private String description;

        private String organizer;

        private String name;

        private String cordinator;

        private String image;

        private String cordinator_contact_no;

        public String getTime ()
        {
            return time;
        }

        public void setTime (String time)
        {
            this.time = time;
        }

        public String getLocation ()
        {
            return location;
        }

        public void setLocation (String location)
        {
            this.location = location;
        }

        public String getContact_no ()
        {
            return contact_no;
        }

        public void setContact_no (String contact_no)
        {
            this.contact_no = contact_no;
        }

        public String getDescription ()
        {
            return description;
        }

        public void setDescription (String description)
        {
            this.description = description;
        }

        public String getOrganizer ()
        {
            return organizer;
        }

        public void setOrganizer (String organizer)
        {
            this.organizer = organizer;
        }

        public String getName ()
        {
            return name;
        }

        public void setName (String name)
        {
            this.name = name;
        }

        public String getCordinator ()
        {
            return cordinator;
        }

        public void setCordinator (String cordinator)
        {
            this.cordinator = cordinator;
        }

        public String getImage ()
        {
            return image;
        }

        public void setImage (String image)
        {
            this.image = image;
        }

        public String getCordinator_contact_no ()
        {
            return cordinator_contact_no;
        }

        public void setCordinator_contact_no (String cordinator_contact_no)
        {
            this.cordinator_contact_no = cordinator_contact_no;
        }

}
